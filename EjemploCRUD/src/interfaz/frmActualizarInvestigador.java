    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ManejadorArchivoBinarioInvestigador archivo = new ManejadorArchivoBinarioInvestigador();
        archivo.modificarContenido("investigador.bin", txtCodigo.getText(), new Investigador(txtCodigo.getText(), txtNombre.getText(), txtGenero.getText(), txtContrasenia.getText(), 0));
        JOptionPane.showMessageDialog(null, "Investigador modificado correctamente");
        txtCodigo.setText("");
        txtNombre.setText("");
        txtGenero.setText("");
        txtContrasenia.setText("");
        this.menuAdmin.refrescarTabla();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        ManejadorArchivoBinarioInvestigador archivo = new ManejadorArchivoBinarioInvestigador();
        ArrayList<Investigador> listado = archivo.obtenerContenido("investigador.bin");
        
        for (Investigador investigador : listado) {
            if (investigador.getCodigo().equals(txtCodigo.getText())) {
                txtContrasenia.setText(investigador.getContrasenia());
                txtGenero.setText(investigador.getGenero());
                txtNombre.setText(investigador.getNombre());
                return;
            }
        }
        
        JOptionPane.showMessageDialog(null, "No se ha encontrado un investigador con el codigo ingresado","Error",JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_jButton2ActionPerformed

